export function buildDashboardMetrics(): DashboardMetric[] {
  const countries = new Set(robots.map((robot) => robot.country)).size;
  const deploymentsCount = deployments.reduce((sum, deployment) => sum + deployment.numberOfRobots, 0);
  const avgPrice = averagePrice(robots);
  const aiCapability = Math.round(
    average(
      robots.map((robot) => (robot.capabilityScores.reasoning + robot.capabilityScores.conversation + robot.capabilityScores.vision) / 3)
    )
  );
  const robotCapability = Math.round(
    average(robots.map((robot) => average(Object.values(robot.capabilityScores))))
  );
  const sciFiSimilarity = Math.round(
    average(
      robots.map((robot) =>
        average([
          robot.capabilityScores.walking,
          robot.capabilityScores.dexterity,
          robot.capabilityScores.autonomy,
          robot.capabilityScores.vision,
          robot.capabilityScores.manipulation,
        ])
      )
    )
  );

  return [
    { label: 'Global humanoid count', value: `${robots.length}`, detail: 'Tracked robots in the local intelligence graph', tone: 'accent' },
    { label: 'Average price', value: formatCurrency(avgPrice), detail: 'Current price of priced robots in the dataset', tone: 'muted' },
    { label: 'Countries building humanoids', value: `${countries}`, detail: 'Unique manufacturing countries represented', tone: 'positive' },
    { label: 'Commercial deployments', value: `${deploymentsCount}`, detail: 'Deployed robots across active sites', tone: 'warning' },
    { label: 'AI capability score', value: `${aiCapability}`, detail: 'Conversation + reasoning + perception blend', tone: 'accent' },
    { label: 'Robot capability score', value: `${robotCapability}`, detail: 'Aggregate platform readiness score', tone: 'positive' },
    { label: 'Sci-Fi similarity index', value: `${sciFiSimilarity}`, detail: 'How close current robots are to cinematic archetypes', tone: 'warning' },
    { label: 'Active manufacturers', value: `${new Set(robots.map((robot) => robot.manufacturer)).size}`, detail: 'Companies represented in the platform', tone: 'accent' },
  ];
}

export function averagePrice(items: RobotRecord[]) {
  const prices = items.map((item) => item.price).filter((price) => price > 0);
  if (!prices.length) return 0;
  return Math.round(prices.reduce((sum, price) => sum + price, 0) / prices.length);
}

function average(values: number[]) {
  if (!values.length) return 0;
  return values.reduce((sum, value) => sum + value, 0) / values.length;
}

export function formatCurrency(value: number) {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    maximumFractionDigits: 0,
  }).format(value);
}

export function formatCompact(value: number) {
  return new Intl.NumberFormat('en-US', {
    notation: 'compact',
    maximumFractionDigits: 1,
  }).format(value);
}
